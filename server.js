    analyzeTrendsFromPatterns(patterns) {
        if (patterns.length === 0) return { strongTrend: false };
        
        // Ph√¢n t√≠ch t·∫ßn su·∫•t c√°c pattern
        const patternCounts = {};
        patterns.forEach(p => {
            patternCounts[p] = (patternCounts[p] || 0) + 1;
        });
        
        // T√¨m pattern ph·ªï bi·∫øn nh·∫•t
        let maxCount = 0;
        let mostCommonPattern = '';
        for (const [pattern, count] of Object.entries(patternCounts)) {
            if (count > maxCount) {
                maxCount = count;
                mostCommonPattern = pattern;
            }
        }
        
        // N·∫øu c√≥ pattern xu·∫•t hi·ªán nhi·ªÅu l·∫ßn
        if (maxCount >= 3 && mostCommonPattern.length === 8) {
            const nextResult = patterns[mostCommonPattern] || 'T√†i'; // Fallback
            return {
                strongTrend: true,
                prediction: nextResult === 'T' ? 'T√†i' : 'X·ªâu',
                confidence: Math.min(0.9, 0.7 + (maxCount * 0.05)),
                reason: `Pattern "${mostCommonPattern}" xu·∫•t hi·ªán ${maxCount} l·∫ßn`,
                strength: maxCount >= 5 ? 'R·∫§T M·∫†NH' : maxCount >= 3 ? 'M·∫†NH' : 'TRUNG B√åNH'
            };
        }
        
        return { strongTrend: false };
    }
    
