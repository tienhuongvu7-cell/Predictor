    // ============ THU·∫¨T TO√ÅN 5: C·∫¶U NH·∫¢Y C√ìC (TH√äM M·ªöI) ============
    analyzeJumpingPattern(data, source) {
        if (!data || data.length < 15) return null;
        
        const recentResults = data.slice(0, 15).map(item => this.normalizeResult(item, source));
        
        // Ph√¢n t√≠ch kho·∫£ng c√°ch gi·ªØa c√°c l·∫ßn xu·∫•t hi·ªán
        let taiPositions = [];
        let xiuPositions = [];
        
        recentResults.forEach((result, index) => {
            if (result === 'T√†i') {
                taiPositions.push(index);
            } else {
                xiuPositions.push(index);
            }
        });
        
        // T√≠nh to√°n xu h∆∞·ªõng nh·∫£y c√≥c
        if (taiPositions.length >= 5 && xiuPositions.length >= 5) {
            // Ki·ªÉm tra xem c√≥ ph·∫£i nh·∫£y c√≥c sau 3-5 c·ª≠a kh√¥ng
            let isValidJump = true;
            let jumpPattern = [];
            
            // Ph√¢n t√≠ch c√°c kho·∫£ng c√°ch
            for (let i = 1; i < taiPositions.length; i++) {
                const gap = taiPositions[i] - taiPositions[i-1];
                jumpPattern.push(gap);
                
                if (gap < 3 || gap > 6) { // Kho·∫£ng c√°ch 3-6 l√† nh·∫£y c√≥c
                    isValidJump = false;
                    break;
                }
            }
            
            if (isValidJump && jumpPattern.length >= 2) {
                // D·ª± ƒëo√°n d·ª±a tr√™n kho·∫£ng c√°ch trung b√¨nh
                const avgGap = jumpPattern.reduce((a, b) => a + b, 0) / jumpPattern.length;
                const lastGap = jumpPattern[jumpPattern.length - 1];
                
                let prediction;
                if (lastGap >= avgGap) {
                    // Kho·∫£ng c√°ch tƒÉng -> c√≥ th·ªÉ s·∫Øp ƒë·ªïi
                    prediction = recentResults[0] === 'T√†i' ? 'X·ªâu' : 'T√†i';
                } else {
                    // Kho·∫£ng c√°ch gi·∫£m -> ti·∫øp t·ª•c
                    prediction = recentResults[0];
                }
                
                return {
                    prediction: prediction,
                    confidence: 0.75,
                    method: `C·∫ßu nh·∫£y c√≥c (kho·∫£ng c√°ch ${lastGap}) ‚Üí ${prediction}`,
                    jumpPattern: jumpPattern,
                    type: 'JUMPING'
                };
            }
        }
        
        return null;
    }
    
